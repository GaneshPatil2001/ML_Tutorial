import numpy as np
arr1= np.array([1,2,3])
print(arr1)
arr2=np.array([4,5,7])
print(arr2)

#Addition
print("Addition: ",arr1+arr2)

 #Substraction
print("Substraction: ",arr2-arr1)

#Division
print("Division: ",arr2/arr1)

#Floor Division
print("Floor Division: ",arr2//arr1)

#Modulus 
print("Modulus: ",arr2%arr1)

#Multiplication
print("Multiplication: ",arr1*arr2)

